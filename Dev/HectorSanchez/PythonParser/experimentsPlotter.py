def cymkToRGB(C,Y,M,K):
    """
    Description:
        *
    In:
        *
    Out:
        *
    Notes:
        * CYMK must be fractional values ranging from 0 to 1
    """
    r=255*(1-C)*(1-K)
    g=255*(1-M)*(1-K)
    b=255*(1-Y)*(1-K)
    return "rgb("+str(r)+","+str(g)+","+str(b)+")"
